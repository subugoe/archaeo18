				<p>Die <a href="">Online-Edition</a> „Christian Gottlob Heyne – Vorlesungen über die Archäologie“ stellt Digitalisate und Transkriptionen von Vorlesungsmitschriften zu einer jährlich gehaltenen Vorlesung <a href="" title="">Christian Gottlob Heynes</a> zur Verfügung. Die <a href="" title="">Edition</a> bietet neben dem Zugriff auf die Digitalisate und der Suche in den Volltexten die Möglichkeit, gezielt nach <a href="" title="">Personen</a>, <a href="" title="">Orten</a>, <a href="" title="">Literatur</a> und <a href="" title="">Kunstwerken</a> zu suchen. Die identifizierten Entitäten sind durch die Verlinkung in <a href="" title="">externe Datenbanken</a> normiert, die in den Mitschriften genannte Literatur wurde – sofern noch nicht in einem anderen <a href="" title="">Repositorium</a> vorhanden – digitalisiert und ebenfalls mit der entsprechenden Stelle im Volltext verknüpft.</p>
